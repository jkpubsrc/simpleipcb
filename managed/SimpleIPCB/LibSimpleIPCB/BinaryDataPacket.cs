using System;
using System.Text;



namespace LibSimpleIPCB
{

	public class BinaryDataPacket : AbstractDataPacket
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		public byte[] Data;

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		public BinaryDataPacket()
		{
		}

		public BinaryDataPacket(byte[] data)
		{
			Data = data;
		}

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

		public override byte[] ToArray()
		{
			if (Data == null) {
				return new byte[0];
			} else {
				return Data;
			}
		}

	}

}

