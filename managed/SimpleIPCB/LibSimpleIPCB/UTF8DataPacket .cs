using System;
using System.Text;



namespace LibSimpleIPCB
{

	public class UTF8DataPacket : AbstractDataPacket
	{

		////////////////////////////////////////////////////////////////
		// Constants
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Variables
		////////////////////////////////////////////////////////////////

		public string Data;

		////////////////////////////////////////////////////////////////
		// Constructors
		////////////////////////////////////////////////////////////////

		public UTF8DataPacket()
		{
		}

		public UTF8DataPacket(byte[] data)
		{
			Data = Encoding.UTF8.GetString(data);
		}

		////////////////////////////////////////////////////////////////
		// Properties
		////////////////////////////////////////////////////////////////

		////////////////////////////////////////////////////////////////
		// Methods
		////////////////////////////////////////////////////////////////

		public override byte[] ToArray()
		{
			if (Data == null) {
				return Encoding.UTF8.GetBytes("");
			} else {
				return Encoding.UTF8.GetBytes(Data);
			}
		}

	}

}

